package java05_array.array2D;

public class Array2D_07 {
	public static void main(String[] args) {

		int[] arr = {10,20,30}; //참조형변수arr, 배열
		System.out.println(arr); //참조값 출력
		
		String str = "Apple"; //참조형변수str, 문자열
		System.out.println(str); //문자열 출력
		
		// 참조형 변수를 출력하면 참조값이 출력된다
		// 단, String은 편의상 실 데이터를 출력하게 만들어져있다
	}
}













