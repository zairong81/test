package java02_operator;

public class BinaryEx_03 {
	public static void main(String[] args) {
		//이항연산자 - 관계, 비교
		
		//	<	lt, less than
		//	>	gt, greater than
		//	<=	le, less than equal
		//	>=	ge, greater than equal
		//	==	eq, equal
		//	!=	ne, not equal
		
		// 연산의 결과가 참/거짓 (boolean타입, true/false)
		
		int num1 = 10;
		int num2 = 20;
		
		System.out.println("num1==num2 : " + (num1==num2) );
		System.out.println("num1!=num2 : " + (num1!=num2) );
		
		System.out.println("num1<num2 : " + (num1<num2) );
		System.out.println("num1>num2 : " + (num1>num2) );
		
		System.out.println("num1<=num2 : " + (num1<=num2) );
		System.out.println("num1>=num2 : " + (num1>=num2) );
		
	}
}














