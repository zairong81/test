package java01_variables;

public class VariablesEx_06 {
	public static void main(String[] args) {
	
		//String, 문자열
		//	문장을 표현하는 데이터타입
	
		String fruit; //문자열 변수 선언
		fruit = "Apple"; //문자열 "Apple" 저장(대입)
		System.out.println( fruit ); //문자열변수 fruit 출력
		
		fruit = "grape";
		System.out.println( fruit );
	}
}






