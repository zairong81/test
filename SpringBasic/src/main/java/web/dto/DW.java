package web.dto;

public class DW {
	
	int D;
	String W;
	
	@Override
	public String toString() {
		return "DW [D=" + D + ", W=" + W + "]";
	}

	public int getD() {
		return D;
	}

	public void setD(int d) {
		D = d;
	}

	public String getW() {
		return W;
	}

	public void setW(String w) {
		W = w;
	}
	
	
	
	

	
	
	
}
